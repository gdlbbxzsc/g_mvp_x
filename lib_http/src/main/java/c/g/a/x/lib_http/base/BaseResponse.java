package c.g.a.x.lib_http.base;

import java.io.Serializable;

public abstract class BaseResponse implements Serializable {

    private int code;

    private String msg;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
